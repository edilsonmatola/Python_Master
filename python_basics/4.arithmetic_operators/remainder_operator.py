# The / operator performs division. 
# However, if we want to find the quotient instead, we use the // operator.

divisor = 4
dividend = 25

# compute quotient
quotient = dividend // divisor

print('Quotient:', quotient)    # Output: 6


# The % operator computes remainder when one number is divided by another.

divisor = 4
dividend = 25

# compute remainder
remainder = dividend % divisor

print('Remainder:', remainder)    # Output: 1
