# Integers (int) are numbers without decimal parts. For example, 5, -11, 0, 12, etc.
int_1 = 5
int_2 = -11
int_3 = 0
int_4 = 12
int_5 = 13454

# Floating-point (float) numbers contain decimal parts. For example, 2.5, 6.76, 0.0, -9.45, etc.
float_1 = 2.5
float_2 = 6.75
float_3 = 0.0
float_4 = -9.45
float_5 = 13.98

# Printing Numbers
print(65) #output: 65

# Printing multiple numbers
print(int_1, float_3) #output: 5 0.0

# Printing in new line (\n)

print('Hey.\n\nHow you are?')
