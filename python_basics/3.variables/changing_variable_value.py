# We can change the value stored in variables as per our needs.

age = 25
print(age)  # Output: 25

age = 26
print(age)  # Output: 26

var = 25
var = 'John'

print(var)  # Output: John


# Value of One Variable to Another
# It's also possible to assign the value of one variable to another.

color1 = 'blue'
print(color1)  # Output: blue

color2 = 'pink'

# Assigning the value stored in color2 to color1
color1 = color2

print(color1)  # Output: pink
